/**
 * Writes src/lib/imageManifest.generated.ts — the set of image paths that
 * actually exist under public/.
 *
 * Run: node tools/gen-image-manifest.mjs   (wired into `prebuild`)
 *
 * WHY THIS EXISTS: the menu page used to answer "has this dish been shot yet?"
 * with fs.existsSync(path.join(process.cwd(), "public", image)) at render time.
 * That works on Node because the page is statically prerendered, so the check
 * runs on the build machine. It does not survive Cloudflare Workers: there is
 * no filesystem and no meaningful process.cwd(), so any render that ever
 * happened at runtime would report every photo missing and fall back to
 * placeholders for the whole menu. It also drags node:fs into the Worker
 * bundle for no reason.
 *
 * Resolving it here instead keeps exactly the same behaviour, moves the answer
 * to a plain string set, and leaves the runtime graph free of node:fs.
 */
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const publicDir = path.join(root, "public");
const dest = path.join(root, "src", "lib", "imageManifest.generated.ts");

// Walk public/ and collect every file as a site-absolute path ("/images/x.png").
async function walk(dir, prefix = "") {
  const found = [];
  for (const entry of await fs.readdir(dir, { withFileTypes: true })) {
    const rel = `${prefix}/${entry.name}`;
    if (entry.isDirectory()) found.push(...(await walk(path.join(dir, entry.name), rel)));
    else found.push(rel);
  }
  return found;
}

const files = (await walk(publicDir)).sort();

const body = `// GENERATED BY tools/gen-image-manifest.mjs — DO NOT EDIT BY HAND.
// Regenerate with \`npm run gen:images\` (also runs automatically on \`prebuild\`).
//
// Every asset present under public/ at build time. The menu page uses this to
// decide whether a dish has a photo yet, instead of probing the filesystem at
// render time — which the Cloudflare Workers runtime cannot do.

const PUBLIC_FILES = new Set<string>([
${files.map((f) => `  ${JSON.stringify(f)},`).join("\n")}
]);

/** True when \`src\` (a site-absolute path like "/images/galbi.png") exists. */
export const publicFileExists = (src: string): boolean => PUBLIC_FILES.has(src);
`;

await fs.writeFile(dest, body);
console.log(`imageManifest: ${files.length} files -> src/lib/imageManifest.generated.ts`);
